class Calculus {

    operation(a,b,operand){
        switch (operand) {
            case "+": return a + b;
                
                break;
            case "-": return a - b;
                
                break;
            case "*": return a * b;
                
                break;
            case "/": return a / b;
                
                break;
            default:
                break;
        }
    }
}